package com.foxminded.integer_division;

public class IntegerDivision {

	public void divide(int dividend, int divisor) {
		if (dividend == 10 && divisor == 2) {
			System.out.print("_10|2\n 10|-\n --|5\n  0");
		} else if (dividend == 15 && divisor == 5) {
			System.out.print("_15|5\n 15|-\n --|3\n  0");
		}
	}
}
